const dictionaries: Record<Lang, typeof ru> = { ru, en, tr };

interface I18nState {
  lang: Lang;
  setLang: (l: Lang) => void;
}

const initialLang: Lang = (() => {
  try {
    const saved = localStorage.getItem('miz_lang') as Lang | null;
    if (saved === 'ru' || saved === 'en' || saved === 'tr') return saved;
  } catch {}
  const nav = (navigator.language || 'ru').slice(0, 2).toLowerCase();
  if (nav === 'en') return 'en';
  if (nav === 'tr') return 'tr';
  return 'ru';
})();

export const useI18n = create<I18nState>((set) => ({
  lang: initialLang,
  setLang(l) {
    try { localStorage.setItem('miz_lang', l); } catch {}
    set({ lang: l });
  },
}));

export type TKey = keyof typeof ru;

/** Get the translation function for the current language. */
export function useT() {
  const lang = useI18n((s) => s.lang);
  const dict = dictionaries[lang];
  const t = (key: TKey): string => (dict as any)[key] ?? (ru as any)[key] ?? String(key);
  return { t, lang };
}

/** Standalone t() that reads the current language from the store — used outside React. */
export function t(key: TKey): string {
  const lang = useI18n.getState().lang;
  const dict = dictionaries[lang];
  return (dict as any)[key] ?? (ru as any)[key] ?? String(key);
}

